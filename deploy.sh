#!/bin/bash
# Скрипт для сборки и деплоя проекта на GitHub Pages

echo "Начинаем сборку проекта..."

# Очищаем директорию dist
npm run clean

# Запускаем сборку проекта
npm run build

echo "Сборка завершена успешно!"

echo "Подготовка к публикации на GitHub Pages..."

# Копируем собранные файлы в корневую директорию для GitHub Pages
cp -r dist/* .

echo "Файлы скопированы. Готово к коммиту и публикации!"